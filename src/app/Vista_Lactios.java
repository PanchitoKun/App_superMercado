    private void btn_ingresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ingresarActionPerformed

  

        /*el botton para ingresar los valores del producto */

        if (txt_stook.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "No existe stook , mas rapido para la otra ");
        }
        else{

            /*while (txt_stook.getText()=="") {
                JOptionPane.showMessageDialog(this, "No existe stook ");
            }*/

            Producto o_Producto= new Producto();
            o_Producto.setId(Integer.parseInt(txt_id.getText()));
            o_Producto.setNombre(txt_nombre.getText());
            o_Producto.setDescripcion(txt_descripcion.getText());
            o_Producto.setStock(Integer.parseInt(txt_stook.getText()));
            o_Producto.setPrecio(Integer.parseInt(txt_precio.getText()));
            

            lista_producto.add(o_Producto);

            JOptionPane.showMessageDialog(this, "producto ingresado ");

            cbo_Que_producto_es.setEnabled(false);

        }
        // TODO add your handling code here:
    }//GEN-LAST:event_btn_ingresarActionPerformed

